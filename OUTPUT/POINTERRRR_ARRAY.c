
#include<stdio.h>
int main()
{
    int a[4]={5,4,7,9};
    int *pt;
    pt=&a;
    printf("%d\n",pt);
    printf("%d\n",*pt);        /// শুধু  pt হলে a এর প্রথম ইন্ডেক্সের এড্রেস প্রিন্ট করবে, *pt হলে ১ম ইন্ডেক্সের ভ্যালু দিবে
    printf("%d %d %d %d",pt[0],pt[1],pt[2],pt[3]);  /// এরের ভ্যালু প্রিন্ট করতে (*) দিতে হয় না । কিন্তু অবশ্যই এরে প্রিন্টের মত করতে হবে।



    return 0;
}
